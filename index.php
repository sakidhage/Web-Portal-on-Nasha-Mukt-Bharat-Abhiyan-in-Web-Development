						 <div id="block-system-main" class="block block-system no-title" >          <div class="splightinner">  <p><img src="images\nm.jpg" alt="Nasha Mukt Bharat Abhiyaan" width="1250" height="290" /></p>  <p>The <a href="https://www.india.gov.in/official-website-ministry-social-justice-and-empowerment" target="_blank">Minister for Social Justice &amp; Empowerment</a> Shri Thawaarchand Gehlot launched the <a href="http://nmba.dosje.gov.in/" target="_blank">website for the Nasha Mukt Bharat Abhiyaan (NMBA)</a> on the occasion of International Day Against Drug Abuse and Illicit Trafficking on 26 June 2021, which is observed worldwide to strengthen action and cooperation in achieving the goal of a sustainable world free of substance abuse. The entire world is facing the menace of drug addiction which has a devastating impact on the addict, individual, family and a large section of society.</p>  <p>The launch of <a href="http://nmba.dosje.gov.in/" target="_blank">Nasha Mukt Bharat Abhiyaan (NMBA)</a> is for 272 Districts across 32 State/Union Territories that have been identified as the most vulnerable in terms of usage of drugs in the country. These vulnerable districts were identified on the basis of findings from the Comprehensive National Survey and the inputs provided by the <a href="https://www.india.gov.in/official-website-narcotics-control-bureau-india" target="_blank">Narcotics Control Bureau (NCB)</a>. According to the National Comprehensive Survey conducted by the <a href="https://www.india.gov.in/official-website-ministry-social-justice-and-empowerment" target="_blank">Ministry of Social Justice and Empowerment</a>, there are more than 60 million drug users in the country of which a large number of users are in the age group of 10-17 years.</p>  <div class="clr"></div>  <p class="heading_n">Objectives</p>   <p><a href="http://nmba.dosje.gov.in/">Nasha Mukt Bharat Abhiyaan</a> intends to reach out to the masses and spread awareness about substance abuse through various activities like:</p>        
                <ul>              <li>Awareness generation programmes</li>  <li>Focus on higher educational Institutions, university campuses and schools</li>  <li>Reaching out to the Community and identifying dependent population</li>  <li>Focus on counselling and treatment facilities in hospitals and rehabilitation centres that have been geo-tagged</li>  <li>Capacity building programmes for service providers</li>          </ul>      
                    <div class="clr"></div>        
                      <p class="heading_n">Initiatives</p>   <p>The Ministry of Social Justice and Empowerment has been mandated to reduce drug demand. It coordinates and oversees all aspects of drug abuse prevention including assessment of the extent of the problem, preventive action, treatment and rehabilitation of addicts, dissemination of information, and public awareness and is mandated to run de-addiction centres.</p>   <p><a href="http://nmba.dosje.gov.in/" target="_blank">Nasha Mukt Bharat Abhiyaan</a> is operational with the involvement of more than 500 voluntary organizations across the country, which are assisted financially under the <a href="https://grants-msje.gov.in/display-napddr-action-plan" target="_blank">National Action Plan for Drug  Demand Reduction (NAPDDR)</a> scheme of the <a href="https://www.india.gov.in/official-website-ministry-social-justice-and-empowerment" target="_blank">Ministry of Social Justice &amp; Empowerment</a>. </p>  <p>These NGOs have been actively involved in the implementation of Drug-Free India campaign. Nearly 8000 youth volunteers and outreach workers of these institutions/organizations have gone from door to door, village to village and nearby localities, etc. to educate the people about the ill effects of drug abuse and have assisted in the rehabilitation of victims of substance abuse.</p>  <p>The <a href="http://nmba.dosje.gov.in/" target="_blank">Nasha Mukt Bharat Abhiyaan website</a> provides detailed information about the Abhiyaan and its activities, gives glimpses through the Photo &amp; video gallery, provides IEC resource material and information on the institutions set up by the Ministry with the aim of Drug Demand reduction.</p>                     <div class="clr"></div>         
                         <p class="heading_n">Institutions</p>   <p>The Ministry of Social Justice and Empowerment</a> supports organizations that work for preventive education  awareness generation on substance abuse, capacity building, treatment and rehabilitation. These organizations are:</p>          <div class="clr"></div>        
                           <div align="center"><img src="images\nm2.jpg" alt="Nasha Mukt Bharat Abhiyaan Institutions" width="530" height="211"><br/>  <div align="center"><br/>Institutions supported by Ministry of Social Justice and Empowerment</div></div>    <div class="clr"></div>    <ul><li><strong>Integrated Rehabilitation Center For Addicts (IRCAs)</strong>: are de-addiction centres with inpatient facilities of counselling and treatment for drug dependent persons</li>  <li><strong>Community Peer Led Intervention (CPLI)</strong>: work with the community supported by youth volunteers for early preventive education, especially for vulnerable adolescents and youth in the community</li>  <li><strong>Outreach And Drop In Centres (ODIC)</strong>: provide facilities of screening, assessment and counselling along with providing referral & linkage to treatment and rehabilitation services for drug dependents</li>  <li><strong>GEO Location</strong>: Ministry supported institutions providing Drug addiction counseling, treatment and rehabilitation and other facilities have been Geo-tagged to make their services accessible and easier to locate</li></ul>  <br/>  <div class="clr"></div>  <div class="roundDiv1"><b class="rtop"><b class="r1"></b><b class="r2"></b> <b class="r3"></b> <b class="r4"></b></b>    
